    INVALID_BIRTH_DATE = "INVALID_BIRTH_DATE",
    INVALID_PROVINCE = "INVALID_PROVINCE",
    TOO_MANY_INTERESTS = "TOO_MANY_INTERESTS",
    INVALID_INTEREST = "INVALID_INTEREST",

    // --- Community & Membership Errors ---
    COMMUNITY_NOT_FOUND = "COMMUNITY_NOT_FOUND",
    COMMUNITY_NAME_EXISTS = "COMMUNITY_NAME_EXISTS",
    COMMUNITY_DELETED = "COMMUNITY_DELETED",
    USER_NOT_MEMBER = "USER_NOT_MEMBER",
