    INTERNAL_ERROR = "INTERNAL_ERROR",
    BAD_REQUEST = "BAD_REQUEST",
    VALIDATION_ERROR = "VALIDATION_ERROR", // Kept for general client-side validation
    SERVICE_UNAVAILABLE = "SERVICE_UNAVAILABLE",
    NO_FIELDS_TO_UPDATE = "NO_FIELDS_TO_UPDATE",
    INVALID_ID = "INVALID_ID",
    PAGINATION_INVALID = "PAGINATION_INVALID",
    FORBIDDEN = "FORBIDDEN",

    // --- Auth & Token Errors ---
    INVALID_CREDENTIALS = "INVALID_CREDENTIALS",
    INVALID_TOKEN = "INVALID_TOKEN",
    TOKEN_EXPIRED = "TOKEN_EXPIRED", // Note: Backend might use INVALID_TOKEN for this
    INVALID_CLAIMS = "INVALID_CLAIMS",
    INVALID_ISSUER = "INVALID_ISSUER",
    INVALID_AUDIENCE = "INVALID_AUDIENCE",
    TOKEN_INVALIDATED = "TOKEN_INVALIDATED",
    LOGIN_METHOD_MISMATCH = "LOGIN_METHOD_MISMATCH",

    // --- Verification & OTP Errors ---
    EMAIL_NOT_VERIFIED = "EMAIL_NOT_VERIFIED",
    EMAIL_ALREADY_VERIFIED = "EMAIL_ALREADY_VERIFIED",
    INVALID_OTP = "INVALID_OTP",
    OTP_EXPIRED = "OTP_EXPIRED",
    VERIFICATION_TOKEN_INVALID = "VERIFICATION_TOKEN_INVALID", // Kept for frontend logic

    // --- User & Profile Errors ---
    USER_NOT_FOUND = "USER_NOT_FOUND",
    USERNAME_EXISTS = "USERNAME_EXISTS",
    EMAIL_EXISTS = "EMAIL_EXISTS",
    USER_INACTIVE = "USER_INACTIVE",
    INVALID_GENDER = "INVALID_GENDER",
